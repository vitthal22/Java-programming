
public class Enchance_Loop {

	public static void main(String[] args) {
		
		//enhanced loop (for each loop)
		
		int arr1[] = {10,20,30,40,50};
		
		for(int ele:arr1)
		{
			System.out.println(ele);
		}
		
		String names[] = {"sad", "efsc", "ffddg"};
		
		for(String s:names)
		{
			System.out.println(s);
		}

	}

}
